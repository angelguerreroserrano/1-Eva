# Para escribir cualquier cosa utilizamos el comando print(), y pondremos el texto entre comillas simples ('hola') o comillas dobles ("hola")
print ("Hola mundo")

# Dentro de las comillas podemos añadir carácteres especiales como \n, el caracter de nueva línea o \t, el caracter de tabulación
print ("\tEsto está tabulado. \nEsto está en la segunda línea.")

# Si queremos que estos carácteres se añadan al texto pondremos la letra r antes de las ""
print (r"Ahora \n se verá incluido en el texto")

# También podemos hacer operaciones con palabras como + o *
a = "uno"
b = "dos"
print (a+b)  # unodos
print (a*3)  # unounouno
